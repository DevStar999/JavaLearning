public class PrintThings {
    public static void main(String[] args) {
        System.out.print("First program of new package");
        // Currently after printing this, the cursor is on the same line.

        System.out.println(", this is now the end of first line");
        // Due to the 'println()' method the cursor will now change line.

        System.out.print("This will be written on the second line");
    }
}
